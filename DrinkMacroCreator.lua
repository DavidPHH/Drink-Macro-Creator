local function SetBestDrink()
    for _, drink in pairs(drinks) do
        local drinkitem = GetItemInfo(drink);
        local drinkitemLvl = select(5, GetItemInfo(drink));
        local drinkitemCount = GetItemCount(drink);
        if (drinkitem ~= nil and drinkitemCount ~= nil and drinkitemLvl ~= nil) then
            if (drinkitemCount > 0) and (drinkitemLvl <= UnitLevel("player")) then 
                bestDrink = drinkitem;
                return;
            end
        end
    end
end

local function SetBestFood()
    for _, food in pairs(foods) do
        local fooditem = GetItemInfo(food);
        local fooditemLvl = select(5, GetItemInfo(food));
        local fooditemCount = GetItemCount(food);
        if (fooditem ~= nil and fooditemCount ~= nil and fooditemLvl ~= nil) then
            if (fooditemCount > 0) and (fooditemLvl <= UnitLevel("player")) then 
                bestFood = fooditem;
                return;
            end
        end
    end
end

local function makeMacros()
    -- Drinks
    local oldBestDrink = bestDrink; -- Avoids unnecessary edits
    local macroName = "dmcDrink";
    SetBestDrink();
    local iMacroDrink = GetMacroIndexByName(macroName);
    if (iMacroDrink == 0 and bestDrink ~= nil) then
        CreateMacro(macroName, "INV_MISC_QUESTIONMARK", "#showtooltip\n/use "..bestDrink);
    elseif (oldBestDrink ~= bestDrink) then
        EditMacro(iMacroDrink, macroName, "INV_MISC_QUESTIONMARK", "#showtooltip\n/use "..bestDrink);
    end

    -- Food
    local oldBestFood = bestFood;
    local macroName = "dmcFood";
    SetBestFood();
    local iMacroFood = GetMacroIndexByName(macroName);
    if (iMacroFood == 0 and bestFood ~= nil) then
        CreateMacro(macroName, "INV_MISC_QUESTIONMARK", "#showtooltip\n/use "..bestFood);
    elseif (oldBestFood ~= bestFood) then
        EditMacro(iMacroFood, macroName, "INV_MISC_QUESTIONMARK", "#showtooltip\n/use "..bestFood);
    end
end

local function eventHandler(self, event, ...)
    if (InCombatLockdown()) then
        return
    end
    -- If not in combat, make a macro or edit existing one
    if (event == "PLAYER_ENTERING_WORLD") then
        inWorld = true;
        makeMacros();
    end
    if (event == "BAG_UPDATE" and inWorld) then
        makeMacros();
    end
    if (event == "PLAYER_REGEN_ENABLED" and inWorld) then -- Exiting combat
        makeMacros();
    end
end

frame:SetScript("OnEvent", eventHandler);
